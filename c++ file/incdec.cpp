#include <iostream>
using namespace std;

int main(){

    // inermeant and decremeant
    
    int a = 6;
    int b = 6;

    // postincreant
    cout << a << endl;
    cout << a++ << endl;
    cout << a << endl << endl;

    //preincreant
    cout << b << endl;
   cout << ++b << endl;
    cout << b << endl;


    cin.get();
    return 0;
}

